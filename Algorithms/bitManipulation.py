# Decimal to Binary
b = bin(5)
print(b)

# Hexadecimal (Base 16)
h = hex(5)
print(h)

# Convert Binary to Decimal
binary_5 = '101'
d= int(binary_5,2)
print(d)

# Bitwiser AND
print(5&7)

# Bitwise OR
print(5|7)

# Bitwise XOR
print(5^7)

# Bitwise NOT
print(~5)

# Left Shift (2 pos)
print(5 << 2)

# Arithmetic (Signed) Right Shift (1 pos)
print(5 >> 1)



